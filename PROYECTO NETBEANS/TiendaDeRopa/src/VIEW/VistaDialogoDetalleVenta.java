    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        if (txtnumventa.getText().equals("")|| txtcant.getText().equals("")||txtvalorunit.getText().equals("")) {
           JOptionPane.showMessageDialog(null, "Error, por favor digite todos los campos");
        }else{
             int numventa = Integer.parseInt(txtnumventa.getText());
            //prenda
             int codprenda = prenda.getid(cmbprenda.getSelectedItem().toString());
            //fin prenda
            int cantidad = Integer.parseInt(txtcant.getText());
            double valoruni = Double.parseDouble(txtvalorunit.getText());
            
         String mensaje =  detalleventa.agregar(numventa, codprenda, cantidad, valoruni);
            JOptionPane.showMessageDialog(null, mensaje);

             cmbprenda.setSelectedItem(prenda.getNombre(codprenda));
            limpiarTablaDetalleVentas();
            listarDetalleVenta(numventa);
        }
        
       
    }//GEN-LAST:event_btnagregarActionPerformed

    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        int index = tabladetalleventas.getSelectedRow();
        int consecutivo = Integer.parseInt(String.valueOf(tabladetalleventas.getValueAt(index, 0)));
        int codprenda = Integer.parseInt(String.valueOf(tabladetalleventas.getValueAt(index, 2)));
        int  cantidad = Integer.parseInt(String.valueOf(tabladetalleventas.getValueAt(index, 3)));
        double  valunitario = Double.parseDouble(String.valueOf(tabladetalleventas.getValueAt(index, 4)));
       
        
      
        txtconsecutivo.setText(""+consecutivo);
        
        cmbprenda.setSelectedItem(prenda.getNombre(codprenda));
        txtcant.setText(""+cantidad);
        txtvalorunit.setText(""+valunitario);
        
        deshabilitarbotones();
       
    }//GEN-LAST:event_btneditarActionPerformed

    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        int numventa = Integer.parseInt(txtnumventa.getText());
        //prenda
        int codprenda = prenda.getid(cmbprenda.getSelectedItem().toString());
        //fin prenda
        int cantidad = Integer.parseInt(txtcant.getText());
        double valoruni = Double.parseDouble(txtvalorunit.getText());
        int consecutivo = Integer.parseInt(txtconsecutivo.getText());
        
        String mensaje =detalleventa.editar(numventa, codprenda, cantidad, valoruni,consecutivo);
        
       JOptionPane.showMessageDialog(null, mensaje);
        habilitarbotones();
        limpiarTablaDetalleVentas();
        listarDetalleVenta(numventa);

        txtcant.setText("");
        txtvalorunit.setText("0.0");
      
       
    }//GEN-LAST:event_btnguardarActionPerformed

