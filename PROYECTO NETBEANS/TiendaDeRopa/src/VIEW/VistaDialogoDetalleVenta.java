     public void limpiarTablaDetalleVentas() {
        DefaultTableModel modelo;
         String[] titulos = {"Consecutivo", "numero de venta", "Codigo prenda", "Cantidad","Valor unitario","Subtotal"};
        modelo = new DefaultTableModel(null, titulos);
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
        tabladetalleventas.setModel(modelo);
    }
      public void limpiarcmb(){
       cmbprenda.removeAllItems();
      }
     
