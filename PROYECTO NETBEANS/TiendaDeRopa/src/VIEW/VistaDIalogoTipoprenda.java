    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        if (txtnombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un nombre");
        }else{
            String nombre = txtnombre.getText();
            String mensaje = tipoprenda.agregar(nombre);
            limpiarcajas();
            limpiartablaMateriales();
            tipoprenda.listarTipoPrenda(tablatipoprendas);
            JOptionPane.showMessageDialog(null, mensaje);
        }
    }//GEN-LAST:event_btnagregarActionPerformed

    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        int index = tablatipoprendas.getSelectedRow();
        int id = Integer.parseInt(String.valueOf(tablatipoprendas.getValueAt(index, 0)));
        String nombre = String.valueOf(tablatipoprendas.getValueAt(index, 1));
       
        txtid.setText(""+id);
        txtnombre.setText(""+nombre);
        
        deshabilitarbotones();      
    }//GEN-LAST:event_btneditarActionPerformed

    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        int id = Integer.parseInt(txtid.getText());
        String nombre =txtnombre.getText();
     
        String mensaje = tipoprenda.editar(id, nombre);
        JOptionPane.showMessageDialog(null, mensaje);
        limpiarcajas();
        limpiartablaMateriales();
        tipoprenda.listarTipoPrenda(tablatipoprendas);
        habilitarbotones();
    }//GEN-LAST:event_btnguardarActionPerformed

